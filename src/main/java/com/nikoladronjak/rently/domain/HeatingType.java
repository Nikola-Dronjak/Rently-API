package com.nikoladronjak.rently.domain;

/**
 * Represents different heating types for a residence.
 */
public enum HeatingType {
	/**
	 * Represents the central heating system.
	 */
	Central,
	/**
	 * Represents the gas heating system.
	 */
	Gas,
	/**
	 * Represents the electrical heating system.
	 */
	Electricity,
	/**
	 * Represents the wood heating system(furnace).
	 */
	Wood
}
